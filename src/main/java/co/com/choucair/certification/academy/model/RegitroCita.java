package co.com.choucair.certification.academy.model;

public class RegitroCita {
    private String diacita;
    private String doctorid;
    private String observacion;
    private String cedula;

    public String getDiacita() {
        return diacita;
    }

    public RegitroCita setDiacita(String diacita) {
        this.diacita = diacita;
        return this;
    }



    public String getDoctorid() {
        return doctorid;
    }

    public RegitroCita setDoctorid(String doctorid) {
        this.doctorid = doctorid;
        return this;
    }



    public String getObservacion() {
        return observacion;
    }

    public RegitroCita setObservacion(String observacion) {
        this.observacion = observacion;
        return this;
    }



    public String getCedula() {
        return cedula;
    }

    public RegitroCita setCedula(String cedula) {
        this.cedula = cedula;
        return this;
    }





}


